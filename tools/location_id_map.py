LOCATION_ID_TO_COL_ROW = {v: k for k, v in COL_ROW_TO_LOCATION_ID.items()}


def location_id(col, row):
    """
    Convert die position (col, row) to location_id.
    """
    key = (col, row)
    if key not in COL_ROW_TO_LOCATION_ID:
        raise ValueError(f"invalid die position: col={col}, row={row}")
    return COL_ROW_TO_LOCATION_ID[key]


def col_row(location_id_value):
    """
    Convert location_id to die position (col, row).
    """
    if location_id_value not in LOCATION_ID_TO_COL_ROW:
        raise ValueError(f"invalid location_id: {location_id_value}")
    return LOCATION_ID_TO_COL_ROW[location_id_value]


